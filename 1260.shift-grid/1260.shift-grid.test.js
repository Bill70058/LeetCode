import shiftGrid from './1260.shift-grid'

describe('shift grid', () => {
    test('input [[1,2,3],[4,5,6],[7,8,9]],1 return [[9,1,2],[3,4,5],[6,7,8]]', () => {
        expect(shiftGrid([
            [1, 2, 3],
            [4, 5, 6],
            [7, 8, 9]
        ], 1)).toEqual([
            [9, 1, 2],
            [3, 4, 5],
            [6, 7, 8]
        ]);
    });
    test('input [[3,8,1,9],[19,7,2,5],[4,6,11,10],[12,0,21,13]],4 return [[12,0,21,13],[3,8,1,9],[19,7,2,5],[4,6,11,10]]', () => {
        expect(shiftGrid([
            [3, 8, 1, 9],
            [19, 7, 2, 5],
            [4, 6, 11, 10],
            [12, 0, 21, 13]
        ], 4)).toEqual([
            [12, 0, 21, 13],
            [3, 8, 1, 9],
            [19, 7, 2, 5],
            [4, 6, 11, 10]
        ]);
    });
})